/**
 * Get all news sources for a given country (ISO 3166-1 alpha-2 code).
 */
export function getSourcesByCountry(code: string): NewsSource[] {
  const upper = code.toUpperCase();
  return ALL_NEWS_SOURCES.filter((s) => s.country === upper);
}

/**
 * Get all news sources that cover a given topic.
 */
export function getSourcesByTopic(topic: string): NewsSource[] {
  return ALL_NEWS_SOURCES.filter((s) => s.topics.includes(topic as NewsTopic));
}

/**
 * Get global / regional wire and broadcast sources (country === "GLOBAL").
 */
export function getGlobalSources(): NewsSource[] {
  return ALL_NEWS_SOURCES.filter((s) => s.country === "GLOBAL");
}
